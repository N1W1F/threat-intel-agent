let currentLang = localStorage.getItem("lang") || "ar";
if (!I18N[currentLang]) currentLang = "ar";

function t(key, ...args) {
  const entry = I18N[currentLang][key];
  return typeof entry === "function" ? entry(...args) : entry;
}

// Translate one upgrade-log token (e.g. "apply:fail:Git.Git:timeout") to the
// current language. Tokens are emitted language-neutral by the backend so the
// same event reads correctly whichever locale the user picks.
function translateUpgLog(token) {
  const parts = token.split(":");
  switch (parts[0]) {
    case "scan":
      if (parts[1] === "start") return t("scanStart");
      if (parts[1] === "done") return t("scanDone", parts[2]);
      return token;
    case "apply":
      if (parts[1] === "start") return t("applyStart", parts[2]);
      if (parts[1] === "running") return t("applyRunning", parts.slice(2).join(":"));
      if (parts[1] === "ok") return t("applyOk", parts[2]);
      if (parts[1] === "fail") return t("applyFail", parts[2], parts.slice(3).join(":"));
      return token;
    default:
      return token;
  }
}

function applyStaticTranslations() {
  document.documentElement.lang = currentLang;
  document.documentElement.dir = I18N[currentLang]._dir;
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    el.textContent = t(el.dataset.i18n);
  });
  document.querySelectorAll("[data-i18n-ph]").forEach((el) => {
    el.setAttribute("placeholder", t(el.dataset.i18nPh));
  });
  document.querySelectorAll("[data-i18n-aria]").forEach((el) => {
    el.setAttribute("aria-label", t(el.dataset.i18nAria));
  });
  document.querySelectorAll("[data-i18n-html]").forEach((el) => {
    // trusted, app-authored static content only (help-box step lists) —
    // never used for anything derived from user/network input
    el.innerHTML = t(el.dataset.i18nHtml);
  });
  document.querySelectorAll(".stage-arrow").forEach((el) => {
    el.textContent = I18N[currentLang]._arrow;
  });
}

function setLang(lang) {
  if (!I18N[lang]) return;
  currentLang = lang;
  localStorage.setItem("lang", lang);
  applyStaticTranslations();
  document.querySelectorAll(".lang-opt").forEach((b) => {
    b.setAttribute("aria-pressed", String(b.dataset.lang === lang));
  });
  if (typeof window.onLangChange === "function") window.onLangChange();
}
